#ifndef LLVM_LIB_TARGET_WEATHER_MCTARGETDESC_WEATHERMCRINSTPRINTER_H
#define LLVM_LIB_TARGET_WEATHER_MCTARGETDESC_WEATHERMCRINSTPRINTER_H

#include "MCTargetDesc/WeatherMCTargetDesc.h"
#include "llvm/ADT/StringRef.h"
#include "llvm/MC/MCInst.h"
#include "llvm/MC/MCInstPrinter.h"
#include "llvm/MC/MCSubtargetInfo.h"
#include "llvm/Support/raw_ostream.h"
#include <_types/_uint64_t.h>
#include <utility>

namespace llvm {

class WeatherInstPrinter : public MCInstPrinter {
private:
    // Operand printer.
    void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
    void printImm(const MCInst *MI, unsigned OpNo, raw_ostream &O);
    void printRegName(raw_ostream &OS, MCRegister Reg) override;
public:
    WeatherInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII, const MCRegisterInfo &MRI);

    void printInst(const MCInst *MI, uint64_t Address, StringRef Annot, const MCSubtargetInfo &STI, raw_ostream &O) override;

    // Autogenerated by TableGen from WeatherInstrInfo.td.  Do not edit this manually.
    std::pair<const char *, uint64_t>
    getMnemonic(const MCInst &MI) const override;
    virtual void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
    virtual bool printAliasInstr(const MCInst *MI, uint64_t Address,  raw_ostream &O);
    static const char *getRegisterName(MCRegister Reg);
};

} // end namespace llvm
#endif